def _set_base_style(doc: Document) -> None:
    style = doc.styles["Normal"]
    style.font.name = "Times New Roman"
    style.font.size = Pt(14)
    rpr = style.element.get_or_add_rPr()
    rfonts = rpr.find(qn("w:rFonts"))
    if rfonts is None:
        from docx.oxml import OxmlElement

        rfonts = OxmlElement("w:rFonts")
        rpr.append(rfonts)
    rfonts.set(qn("w:ascii"), "Times New Roman")
    rfonts.set(qn("w:hAnsi"), "Times New Roman")
    rfonts.set(qn("w:cs"), "Times New Roman")
    rfonts.set(qn("w:eastAsia"), "Times New Roman")

    pfmt = style.paragraph_format
    pfmt.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    pfmt.first_line_indent = Cm(1.25)
    pfmt.space_after = Pt(0)
    pfmt.space_before = Pt(0)

    for section in doc.sections:
        section.top_margin = Cm(2)
        section.bottom_margin = Cm(2)
        section.left_margin = Cm(3)
        section.right_margin = Cm(2)


def p(doc, text, *, align=WD_ALIGN_PARAGRAPH.JUSTIFY, bold=False, indent=True, size=None):
    para = doc.add_paragraph()
    para.alignment = align
    if not indent:
        para.paragraph_format.first_line_indent = Cm(0)
    run = para.add_run(text)
    run.bold = bold
    if size:
        run.font.size = Pt(size)
    return para


def h1(doc, text):
    para = doc.add_paragraph()
    para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    para.paragraph_format.first_line_indent = Cm(0)
    para.paragraph_format.space_before = Pt(12)
    para.paragraph_format.space_after = Pt(12)
    run = para.add_run(text.upper())
    run.bold = True
    run.font.size = Pt(14)


def h2(doc, text):
    para = doc.add_paragraph()
    para.alignment = WD_ALIGN_PARAGRAPH.LEFT
    para.paragraph_format.first_line_indent = Cm(1.25)
    para.paragraph_format.space_before = Pt(6)
    para.paragraph_format.space_after = Pt(6)
    run = para.add_run(text)
    run.bold = True


def code(doc, text):
    para = doc.add_paragraph(text)
    para.paragraph_format.first_line_indent = Cm(0)
    para.runs[0].font.name = "Courier New"
    para.runs[0].font.size = Pt(11)


def bullet(doc, text):
    para = doc.add_paragraph(text, style="List Bullet")
    para.paragraph_format.first_line_indent = Cm(0)


def numbered(doc, text):
    para = doc.add_paragraph(text, style="List Number")
    para.paragraph_format.first_line_indent = Cm(0)


def page_break(doc):
    doc.add_page_break()


