from __future__ import annotations

# Минимальный список ключевых слов. Цель — поймать явные обращения; не
# претендуем на полноту классификатора (для этого нужен ML-классификатор,
# выходит за рамки MVP). Список можно расширять по мере появления edge-cases.
_MEDICAL_KEYWORDS: tuple[str, ...] = (
    "болит",
    "боль",
    "травма",
    "грыжа",
    "перелом",
    "давление",
    "сердце",
    "диабет",
    "беремен",
    "месячные",
    "лекарств",
    "таблетк",
    "врач",
    "диагноз",
    "анализ",
)
_MENTAL_KEYWORDS: tuple[str, ...] = (
    "депресс",
    "тревог",
    "паническ",
    "не хочу жить",
    "суицид",
    "психотерап",
    "психолог",
)


DISCLAIMER_TEXT = (
    "Это вопрос к врачу или специалисту — я не могу его консультировать. "
    "Я фитнес-ассистент и помогаю только с тренировками и питанием. "
    "Пожалуйста, обратитесь к профильному специалисту."
)


def is_sensitive(text: str) -> bool:
    haystack = text.strip().lower()
    if not haystack:
        return False
    return any(kw in haystack for kw in _MEDICAL_KEYWORDS) or any(
        kw in haystack for kw in _MENTAL_KEYWORDS
    )
